public class ProdutoCompras {
    
   Produto produto;
   Compras Compras;

    public ProdutoCompras() {
    }

    public ProdutoCompras(Produto produto, br.com.infortec.bean.Compras Compras) {
        this.produto = produto;
        this.Compras = Compras;
    }

    public br.com.infortec.bean.Compras getCompras() {
        return Compras;
    }

    public void setCompras(br.com.infortec.bean.Compras Compras) {
        this.Compras = Compras;
    }

    public Produto getProduto() {
        return produto;
    }

    public void setProduto(Produto produto) {
        this.produto = produto;
    }

}
