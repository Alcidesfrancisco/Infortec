package br.com.infortec.bean;

/**
 *
 * @author Infortec
 */
public class Banco {
    int codBanco;
    String nome;
    String agencia;
    String conta;
    String gerente;
    String fone;
    String obs;
    

     public Banco() {
    }

    public Banco(String nome, String agencia, String conta, String gerente, String fone, String obs) {
        this.nome = nome;
        this.agencia = agencia;
        this.conta = conta;
        this.gerente = gerente;
        this.fone = fone;
        this.obs = obs;
        
    }

    public Banco(int codBanco, String nome, String agencia, String conta, String gerente, String fone, String obs) {
        this.codBanco = codBanco;
        this.nome = nome;
        this.agencia = agencia;
        this.conta = conta;
        this.gerente = gerente;
        this.fone = fone;
        this.obs = obs;
    }

    public String getAgencia() {
        return agencia;
    }

    public void setAgencia(String agencia) {
        this.agencia = agencia;
    }

    public int getCodBanco() {
        return codBanco;
    }

    public void setCodBanco(int codBanco) {
        this.codBanco = codBanco;
    }

    public String getConta() {
        return conta;
    }

    public void setConta(String conta) {
        this.conta = conta;
    }

    public String getFone() {
        return fone;
    }

    public void setFone(String fone) {
        this.fone = fone;
    }

    public String getGerente() {
        return gerente;
    }

    public void setGerente(String gerente) {
        this.gerente = gerente;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getObs() {
        return obs;
    }

    public void setObs(String obs) {
        this.obs = obs;
    }
    
    
    
    
}
