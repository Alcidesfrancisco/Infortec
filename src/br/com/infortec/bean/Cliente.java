package br.com.infortec.bean;



/**
 *
 * @author Infortec
 */
public class Cliente {
    private int codCliente;
   
    private String nome;
    private String cpf;
    private String rg;
    private String orgao;
    private String dataNascimento;
    private String profissao;
    private String empresa;
    private String foneEmpresa;
    private String tipoCliente;
    private String cnpj;
    private String referencia;
    private String foneReferencia;
    private String email;

    public Cliente() {
    }

    public Cliente(String nome, String cpf, String rg, String orgao, String dataNascimento, String profissao, String empresa, String foneEmpresa, String tipoCliente, String cnpj, String referencia, String foneReferencia, String email) {
        this.nome = nome;
        this.cpf = cpf;
        this.rg = rg;
        this.orgao = orgao;
        this.dataNascimento = dataNascimento;
        this.profissao = profissao;
        this.empresa = empresa;
        this.foneEmpresa = foneEmpresa;
        this.tipoCliente = tipoCliente;
        this.cnpj = cnpj;
        this.referencia = referencia;
        this.foneReferencia = foneReferencia;
        this.email = email;
    }

    public Cliente(int codCliente, String nome, String cpf, String rg, String orgao, String dataNascimento, String profissao, String empresa, String foneEmpresa, String tipoCliente, String cnpj, String referencia, String foneReferencia, String email) {
        this.codCliente = codCliente;
        this.nome = nome;
        this.cpf = cpf;
        this.rg = rg;
        this.orgao = orgao;
        this.dataNascimento = dataNascimento;
        this.profissao = profissao;
        this.empresa = empresa;
        this.foneEmpresa = foneEmpresa;
        this.tipoCliente = tipoCliente;
        this.cnpj = cnpj;
        this.referencia = referencia;
        this.foneReferencia = foneReferencia;
        this.email = email;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public int getCodCliente() {
        return codCliente;
    }

    public void setCodCliente(int codCliente) {
        this.codCliente = codCliente;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(String dataNascimento) {
        this.dataNascimento = dataNascimento;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getFoneEmpresa() {
        return foneEmpresa;
    }

    public void setFoneEmpresa(String foneEmpresa) {
        this.foneEmpresa = foneEmpresa;
    }

    public String getFoneReferencia() {
        return foneReferencia;
    }

    public void setFoneReferencia(String foneReferencia) {
        this.foneReferencia = foneReferencia;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getOrgao() {
        return orgao;
    }

    public void setOrgao(String orgao) {
        this.orgao = orgao;
    }

    public String getProfissao() {
        return profissao;
    }

    public void setProfissao(String profissao) {
        this.profissao = profissao;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public String getTipoCliente() {
        return tipoCliente;
    }

    public void setTipoCliente(String tipoCliente) {
        this.tipoCliente = tipoCliente;
    }
    
    
}
