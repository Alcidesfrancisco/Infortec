package br.com.infortec.bean;

/**
 *
 * @author Israelly
 */
public class Telefone {
    
    int cod;
    String num;
    String tipo;
    Cliente cliente;

    public Telefone() {
    }

    public Telefone(String num, String tipo, Cliente cliente) {
        this.num = num;
        this.tipo = tipo;
        this.cliente = cliente;
    }

    public Telefone(int cod, String num, String tipo, Cliente cliente) {
        this.cod = cod;
        this.num = num;
        this.tipo = tipo;
        this.cliente = cliente;
    }

    public Cliente getCliente() {
        return cliente;
    }

    public void setCliente(Cliente cliente) {
        this.cliente = cliente;
    }

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
