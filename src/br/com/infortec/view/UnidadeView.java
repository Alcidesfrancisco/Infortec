    public TableModel tabelaModel(List<Unidade> listaUnidade) {
        int n = 0;
        JTable table1;
        UnidadeDAO unidadeDAO = new UnidadeDAO();
        listaUnidade = unidadeDAO.listarTodasUnidades();
        Object[][] data = new Object[listaUnidade.size()][10];
        String[] columnNames = {"Cod Unidade", "Descrião", "Qtde"};

        for (Unidade unidades : listaUnidade) {
            data[n][0] = unidades.getCod();
            data[n][1] = unidades.getDescricao();
            data[n][2] = unidades.getQtde();

            n++;
        }

        table1 = new JTable(data, columnNames);
        table1.setPreferredScrollableViewportSize(new Dimension(500, 300));
        table1.setFillsViewportHeight(true);
        table1.getColumnModel().getColumn(0).setPreferredWidth(100);
        table1.getColumnModel().getColumn(1).setPreferredWidth(300);
        table1.getColumnModel().getColumn(2).setPreferredWidth(300);
        javax.swing.table.TableModel model = table1.getModel();



        table.addMouseListener(new MouseAdapter() {

            public void mouseClicked(MouseEvent e) {
                if (cont % 2 == 1) {
                    editarLinha();
                }
            }
        });
        return model;//adiciona um modela a tabela

    }

    public void editarLinha() {
        int index = table.getSelectedRow();

        txtDecricao.setText((String) table.getValueAt(table.getSelectedRow(), 1));
        txtQtde.setText(String.valueOf(table.getValueAt(table.getSelectedRow(), 2)));

    }

