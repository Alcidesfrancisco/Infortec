    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (cont % 2 == 0) {
            String descricao = txtDecricao.getText();
            int agencia = Integer.valueOf(txtQtde.getText());

            Unidade unidade = new Unidade(descricao, agencia);
            int idUnidade = unidadeDAO.inserirUnidade(unidade);
            listaEditaUnidade.add(unidade);
            table.setModel(tabelaModel(listaEditaUnidade));
            int row = table.getRowCount() - 1;
            if (table.getRowCount() >= 1) {
                table.setRowSelectionInterval(row, row);
            }
            table.scrollRectToVisible(table.getCellRect(row, 0, true));

            txtDecricao.setText("");
            txtQtde.setText("");
        } else {

            String alterDescricao = txtDecricao.getText();
            int alterQtde = Integer.valueOf(txtQtde.getText());

            Unidade unidadeAlterar = listaEditaUnidade.get(table.getSelectedRow());

            unidadeAlterar.setDescricao(alterDescricao);
            unidadeAlterar.setQtde(alterQtde);

            unidadeDAO.alteraBanco(unidadeAlterar, alterQtde);
            listaEditaUnidade.add(unidadeAlterar);
            table.setModel(tabelaModel(listaEditaUnidade));

            txtDecricao.setText("");
            txtQtde.setText("");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
    if (cont % 2 == 0) {      //editar linha
        txtDecricao.setEnabled(true);
        txtQtde.setEnabled(true);
        btEditar.setText("Cancelar Edição");
        txtDecricao.requestFocus();
        cont++;
        editarLinha();

    } else {                //cancelar edição
        txtDecricao.setEnabled(false);
        txtQtde.setEnabled(false);
        btEditar.setText("Editar");
        txtDecricao.requestFocus();
        cont++;
    }
}//GEN-LAST:event_btEditarActionPerformed
